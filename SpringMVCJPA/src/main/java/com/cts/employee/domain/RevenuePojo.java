package com.cts.employee.domain;

public class RevenuePojo {
	
	
	String sectorName;
	String subProcess;
	String projectId;
	String projectName;
	String forcastedValue;
	String actualInvoiceValue;
	String invoiceMonth;
	String invoiceYear;
	String variance;
	
	
	
	
	public String getSubProcess() {
		return subProcess;
	}
	public void setSubProcess(String subProcess) {
		this.subProcess = subProcess;
	}
	public String getSectorName() {
		return sectorName;
	}
	public void setSectorName(String sectorName) {
		this.sectorName = sectorName;
	}
	public String getProjectId() {
		return projectId;
	}
	public void setProjectId(String projectId) {
		this.projectId = projectId;
	}
	public String getProjectName() {
		return projectName;
	}
	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}
	public String getForcastedValue() {
		return forcastedValue;
	}
	public void setForcastedValue(String projectValue) {
		this.forcastedValue = projectValue;
	}
	public String getActualInvoiceValue() {
		return actualInvoiceValue;
	}
	public void setActualInvoiceValue(String actualValue) {
		this.actualInvoiceValue = actualValue;
	}
	public String getInvoiceMonth() {
		return invoiceMonth;
	}
	public void setInvoiceMonth(String invoiceMonth) {
		this.invoiceMonth = invoiceMonth;
	}
	public String getInvoiceYear() {
		return invoiceYear;
	}
	public void setInvoiceYear(String invoiceYear) {
		this.invoiceYear = invoiceYear;
	}
	public String getVariance() {
		return variance;
	}
	public void setVariance(String variance) {
		this.variance = variance;
	}
	
	
	

}
